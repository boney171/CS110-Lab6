const mongoose = require("mongoose");

const reactionSchema = mongoose.Schema(
  {
    type: {
      type: String,
      required: true,
    },
    user: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

module.exports = mongoose.model("Reaction", reactionSchema);